// Helper function to create a destination object
const createDestination = (name: string, flag: string, isRegional: boolean = false) => ({
  name,
  value: name.toLowerCase().replace(/ /g, '-').replace(/[áéíóúüñ]/g, (match) => {
    const map: { [key: string]: string } = { 'á': 'a', 'é': 'e', 'í': 'i', 'ó': 'o', 'ú': 'u', 'ü': 'u', 'ñ': 'n' };
    return map[match] || match;
  }),
  flag,
  isRegional,
});

